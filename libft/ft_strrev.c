#include "libft.h"

void	ft_strrev(char *s)
{
	size_t	i;
	size_t	j;
	char	temp;

	i = 0;
	j = ft_strlen(s) - 1;
	while (s[i] != '\0' && i < j)
	{
		temp = s[i];
		s[i++] = s[j];
		s[j--] = temp;
	}
}
