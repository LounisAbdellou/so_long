#include "libft.h"

long long	ft_atoll(char *str)
{
	size_t		i;
	long long	res;
	int			mult;

	i = 0;
	res = 0;
	mult = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		mult = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			break ;
		else
			res = (res * 10) + (str[i] - 48);
		i++;
	}
	return (res * mult);
}
